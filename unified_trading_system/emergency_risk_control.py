def kill_trading_process():
    """Kill the current trading process"""
    try:
        # Find the trading process
        result = os.popen("ps aux | grep continuous_trading_loop_binance.py | grep -v grep").read()
        
        if result.strip():
            pid = result.split()[1]
            print(f"[{datetime.now()}] EMERGENCY: Killing trading process PID: {pid}")
            os.kill(int(pid), 9)
            return True
        else:
            print("WARNING: Trading process not found")
            return False
            
    except Exception as e:
        print(f"ERROR: Failed to kill process: {e}")
        return False

