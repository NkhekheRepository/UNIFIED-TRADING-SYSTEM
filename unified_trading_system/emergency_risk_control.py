# Configuration
BINANCE_TESTNET_URL = "https://testnet.binancefuture.com"
API_KEY = os.getenv("BINANCE_API_KEY")
API_SECRET = os.getenv("BINANCE_API_SECRET")

# Emergency Risk Parameters
EMERGENCY_LEVERAGE = 10  # Reduced from 40x
EMERGENCY_MAX_POSITIONS = 3  # Drastically reduced
EMERGENCY_POSITION_CAP = 15  # Reduced from $50

def make_hmac_hash(params_str):
    return hmac.new(API_SECRET.encode(), params_str.encode(), hashlib.sha256).hexdigest()

def close_all_positions():
    """Close all open positions to stop losses"""
    print(f"[{datetime.now()}] EMERGENCY: Closing all positions...")
    
    # Get account information
    endpoint = "/fapi/v2/account"
    timestamp = int(time.time() * 1000)
    params_str = f"timestamp={timestamp}"
    signature = make_hmac_hash(params_str)
    
    response = requests.get(
        f"{BINANCE_TESTNET_URL}{endpoint}?{params_str}&signature={signature}",
        headers={"X-MBX-APIKEY": API_KEY}
    )
    
    if response.status_code != 200:
        print(f"ERROR: Failed to get account info: {response.status_code}")
        return False
        
    account_data = response.json()
    positions = account_data.get("positions", [])
    
    # Close all open positions
    for position in positions:
        position_size = float(position.get("positionAmt", 0))
        if abs(position_size) > 0.001:  # Only close non-zero positions
            symbol = position["symbol"]
            # Determine closing side
            side = "sell" if position_size > 0 else "buy"
            
            print(f"Closing position: {symbol} ({abs(position_size)} units)")
            
            # Place closing order
            close_order(symbol, abs(position_size), side)
    
    return True

def close_order(symbol, quantity, side):
    """Place closing order"""
    endpoint = "/fapi/v1/order"
    timestamp = int(time.time() * 1000)
    params_str = f"symbol={symbol}&side={side}&type=MARKET&quantity={quantity}&timestamp={timestamp}"
    signature = make_hmac_hash(params_str)
    
    response = requests.post(
        f"{BINANCE_TESTNET_URL}{endpoint}?{params_str}&signature={signature}",
        headers={"X-MBX-APIKEY": API_KEY},
        params=None # Ensure no duplicate params in body
    )
    
    if response.status_code == 200:
        print(f"SUCCESS: Placed closing order for {symbol}")
        return True
    else:
        print(f"ERROR: Failed to close {symbol}: {response.status_code}")
        return False

